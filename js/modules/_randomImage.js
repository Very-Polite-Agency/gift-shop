//////////////////////////////////////////////////////////
////  Random Images
//////////////////////////////////////////////////////////

const RandomImage = (() => {

  let debug = false;
  let info = { name : 'RandomImage', version : '1.0' };

  let elements = document.querySelectorAll('.js--product-image-placeholder') || [];
  let images = Theme.placeholder.product_images || [];

  //////////////////////////////////////////////////////////
  ////  Print Random Image
  //////////////////////////////////////////////////////////

  const renderRandomImage = () => {
    if ( images.length ) {
      elements.forEach( el => {
        let randomImageSrc = images[Math.floor(Math.random() * images.length)];
        el.setAttribute('data-src', randomImageSrc );
      });
    }
  };

  //////////////////////////////////////////////////////////
  ////  Public Method | Initialize
  //////////////////////////////////////////////////////////

  const init = () => {
    renderRandomImage();
  };

  //////////////////////////////////////////////////////////
  ////  Returned Methods
  //////////////////////////////////////////////////////////

  return {
    init
  };

});
